/*--------------------------------------------------
三个整数a、b、c，由键盘输入，输出其中最小的一个数。
说明：输入输出均为整型数据，输入的数据间用空格间隔。
例如：输入：2 1 3
      输出：1
---------------------------------------------------*/
#include <stdio.h>
int main()
{
	int a,b,c;
	scanf("%d %d %d",&a,&b,&c);
	if(a<b){
		if(a<c){
			printf("%d\n",a);
		} 
		else{
			printf("%d\n",c);
		}
	} 
	else{
		if(b<c){
			printf("%d\n",b);
		} 
		else{
			printf("%d\n",c);
		}
	} 
}
	 

