#include <stdio.h>

int main()
{
	// ! 逻辑取反
	// ~ 按位取反 转换过来的二进制都是补码
	int i = 1;
	printf("%d\n", !i);
	printf("%d\n", ~i);
	// ~ 00000000 00000000 00000000 00000001
	//   11111111 11111111 11111111 11111110 //-2的补码
	//   10000000 00000000 00000000 00000010 //符号位不能变  -2的原码

	i = -2;
	printf("%d\n", !i);
	printf("%d\n", ~i);
	// ~ 11111111 11111111 11111111 11111110 //-2的补码
	//   00000000 00000000 00000000 00000001 //1
}